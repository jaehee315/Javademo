package java001_basic;

public class Java015_operators {
	
	public static void main(String[] args) {
		int x = 3;
		int res = 0;
		res = 3 +2;
		
		System.out.println(res);
		
		//x = x +1;  좌변과 우변이 같을때 사용
		x +=1;
		System.out.printf("x=%d\n", x);
		
		
		//x = x -1; 
		x -= 1;
		System.out.printf("x=%d\n", x);
		
		
		//x = x *1;
		x *=1;
		System.out.printf("x=%d\n", x);
		
		
		
		
	}
}
